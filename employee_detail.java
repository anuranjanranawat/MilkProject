    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
         String sql = "INSERT INTO `milkproject`.`employee_detail` (`EMPLOYEE_ID`, `FIRSTNAME`, `LASTNAME`,`POSITION`,`TITLE`,`GENDER`, `AGE`, `ADDRESS`) VALUES (?,?,?,?,?,?);";
        try {
           ps = connection.prepareStatement(sql);
           ps.setInt(1,parseInt(employeeid.getText()));
           ps.setString(2,firstname.getText());
           ps.setString(3,lastname.getText());
           ps.setString(4,position.getText());
           ps.setString(5,title.getText());
           ps.setString(6,gender.getText());
           ps.setString(7,age.getText());
           ps.setString(8,address.getText());
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null,"Inserted");
       } catch (SQLException ex) {
           Logger.getLogger(BranchDetail.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_saveActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        String sql = "UPDATE `milkproject`.`employee_detail` SET `EMPLOYEE_ID`=?, `FIRST_NAME`=?, `LAST_NAME`=?, `POSITION`=?, `TITLE`=?, `GENDER`=?, `AGE`=?, `ADDRESS`=? WHERE `EMPLOYEE_ID`=?;";
        str1=parseInt(employeeid.getText());
        str2=firstname.getText();
        str3=lastname.getText();
        str4=position.getText();
        str5=title.getText();
        str6=gender.getText();
        str7=age.getText();
        str8=address.getText();
        try {
           ps = connection.prepareStatement(sql);
           ps.setInt(1,str1);
           ps.setString(2,str2);
           ps.setString(3,str3);
           ps.setString(4,str4);
           ps.setString(5,str5);
           ps.setString(6,str6);
           ps.setString(7,str7);
           ps.setString(8,str8);
           ps.setInt(9,str1);
           ps.executeUpdate();
           ps.executeUpdate("commit");
           JOptionPane.showMessageDialog(null,"Updated");
       } catch (SQLException ex) {
           Logger.getLogger(BranchDetail.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_updateActionPerformed

