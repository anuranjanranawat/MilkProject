    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        String sql = "INSERT INTO `milkproject`.`customer_detail` (`CUSTOMER_ID`, `FIRSTNAME`, `LASTNAME`, `GENDER`, `AGE`, `ADDRESS`) VALUES (?,?,?,?,?,?);";
        try {
           ps = connection.prepareStatement(sql);
           ps.setInt(1,parseInt(customerid.getText()));
           ps.setString(2,firstname.getText());
           ps.setString(3,lastname.getText());
           ps.setString(4,gender.getText());
           ps.setString(5,age.getText());
           ps.setString(6,address.getText());
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null,"Inserted");
       } catch (SQLException ex) {
           Logger.getLogger(BranchDetail.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_saveActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        String sql = "UPDATE `milkproject`.`customer_detail` SET `CUSTOMER_ID`=?, `FIRST_NAME`=?, `LAST_NAME`=?, `GENDER`=?, `AGE`=?, `ADDRESS`=? WHERE `CUSTOMER_ID`=?;";
        str1=parseInt(customerid.getText());
        str2=firstname.getText();
        str3=lastname.getText();
        str4=gender.getText();
        str5=age.getText();
        str6=address.getText();
        try {
           ps = connection.prepareStatement(sql);
           ps.setInt(1,str1);
           ps.setString(2,str2);
           ps.setString(3,str3);
           ps.setString(4,str4);
           ps.setString(5,str5);
           ps.setString(6,str6);
           ps.setInt(7,str1);
           ps.executeUpdate();
           ps.executeUpdate("commit");
           JOptionPane.showMessageDialog(null,"Updated");
       } catch (SQLException ex) {
           Logger.getLogger(BranchDetail.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_updateActionPerformed

