    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
         int input;
        try
			{
			  input = parseInt(JOptionPane.showInputDialog("Enter the  CUSTOMER_ID  to be search  :"));
		        ps = connection.prepareStatement("select * from  milkproject.customer_detail  where    CUSTOMER_ID=? ");
                        ps.setInt(1, input);
                        rs = ps.executeQuery();
                        
			while(rs.next())
                        {

			customerid.setText(rs.getString(1));
			firstname.setText(rs.getString(2));
			lastname.setText(rs.getString(3));
			gender.setText(rs.getString(4));
			age.setText(rs.getString(5));
			address.setText(rs.getString(6));
			JOptionPane.showMessageDialog(null,"found");
                        }
                        }
			catch(SQLException sqle)
			{
				JOptionPane.showMessageDialog(null,"not found");
			}
    }//GEN-LAST:event_searchActionPerformed

