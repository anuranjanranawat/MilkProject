    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        int input;
        try
			{
				input = parseInt(JOptionPane.showInputDialog("Enter the  SALARY_CODE_ID  to be Deleted  :"));
		        ps = connection.prepareStatement("DELETE from  milkproject.salary_code_detail  where    SALARY_CODE=? ");
                        ps.setInt(1, input);
                        ps.executeUpdate();
				JOptionPane.showMessageDialog(null,"delete");
			}
			catch(SQLException sqle)
			{
				JOptionPane.showMessageDialog(null,"could not Deleted"+sqle);
			}
    }//GEN-LAST:event_deleteActionPerformed

    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        int input;
        try
			{
			  input = parseInt(JOptionPane.showInputDialog("Enter the  SALARY_CODE_ID  to be search  :"));
		        ps = connection.prepareStatement("select * from  milkproject.salary_code_detail  where    SALARY_CODE=? ");
                        ps.setInt(1, input);
                        rs = ps.executeQuery();
                        
			while(rs.next())
                        {

			salarycode.setText(rs.getString(1));
			grosssalary.setText(rs.getString(2));
			tpr.setText(rs.getString(3));
			csr.setText(rs.getString(4));
			netsalary.setText(rs.getString(5));
			JOptionPane.showMessageDialog(null,"found");
                        }
                        }
			catch(SQLException sqle)
			{
				JOptionPane.showMessageDialog(null,"not found");
			}
    }//GEN-LAST:event_searchActionPerformed

