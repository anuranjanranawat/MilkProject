    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
         String sql = "INSERT INTO `milkproject`.`salary_code_detail` (`SALARY_CODE`, `GROSS_SALARY`, `TPR`, `CSR`, `NET_SALARY`) VALUES (?,?,?,?,?)";
        try {
           ps = connection.prepareStatement(sql);
           ps.setInt(1,parseInt(salarycode.getText()));
           ps.setString(2,grosssalary.getText());
           ps.setString(3,tpr.getText());
           ps.setString(4,csr.getText());
           ps.setString(5,netsalary.getText());
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null,"Inserted");
       } catch (SQLException ex) {
           Logger.getLogger(BranchDetail.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_saveActionPerformed

    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        String sql = "UPDATE `milkproject`.`salary_code_detail` SET `SALARY_CODE`=?, `GROSS_SALARY`=?, `TPR`=?, `CSR`=?, `NET_SALARY`=? WHERE `SALARY_CODE`=?;";
        try {
           ps = connection.prepareStatement(sql);
           ps.setInt(1,parseInt(salarycode.getText()));
           ps.setString(2,grosssalary.getText());
           ps.setString(3,tpr.getText());
           ps.setString(4,csr.getText());
           ps.setString(5,netsalary.getText());
           ps.setInt(7,parseInt(salarycode.getText()));
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null,"Updated");
       } catch (SQLException ex) {
           Logger.getLogger(BranchDetail.class.getName()).log(Level.SEVERE, null, ex);
       }
    }//GEN-LAST:event_updateActionPerformed

